# Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Fahrenheit.
C = float(input('Digite aqui a temperatura em Celsius: '))
prova = ((C/5)*9)+32
print(f'{float(prova)}° graus Fahrenheit')
